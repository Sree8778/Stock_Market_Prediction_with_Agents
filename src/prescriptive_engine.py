"""
Prescriptive Portfolio Intelligence Engine
  analyze_holding(h, price, geo, tech_ind, macro, portfolio_value) -> dict
  get_portfolio_analysis(holdings, prices, geo, tech_map, macro)   -> dict
  generate_reasoning_card(ticker, geo, tech_ind, v_score, macro, rec, action) -> str

Action labels:
  INVEST MORE   — both signals bullish, price above SMA20
  HOLD          — mixed or neutral signals
  PARTIAL SELL  — concentration > 40% of portfolio
  SELL          — both signals bearish
  STOP-LOSS     — live price < stop_loss level

XAI reasoning card is generated by Gemini 2.5 Flash (mock fallback if unavailable).
"""

import os
import re
import time
from pathlib import Path

import diskcache

_CACHE_DIR = str(Path(__file__).parent.parent / "data" / ".prescriptive_cache")
_cache     = diskcache.Cache(_CACHE_DIR)
_TTL       = 3_600      # 1 h — reasoning cards rotate with market conditions

STOP_LOSS_PCT   = 0.05   # trailing stop-loss  5% below live price
TAKE_PROFIT_PCT = 0.15   # take-profit        15% above avg cost
CONCENTRATION_THRESHOLD = 0.40   # flag if > 40% of portfolio

# ── Gemini key resolution ──────────────────────────────────────────────────────
def _get_api_key() -> str:
    key = os.environ.get("GEMINI_API_KEY", "")
    if key:
        return key
    try:
        import streamlit as st
        key = st.secrets.get("GEMINI_API_KEY", "")
        if key:
            return key
    except Exception:
        pass
    try:
        toml = Path(__file__).parent.parent / ".streamlit" / "secrets.toml"
        if toml.exists():
            m = re.search(r'GEMINI_API_KEY\s*=\s*"([^"]+)"', toml.read_text())
            if m:
                return m.group(1)
    except Exception:
        pass
    return ""


def _call_gemini(prompt: str, retries: int = 2) -> str:
    import google.generativeai as genai
    key = _get_api_key()
    if not key:
        raise EnvironmentError("No GEMINI_API_KEY")
    genai.configure(api_key=key)
    client = genai.GenerativeModel("gemini-2.5-flash")
    for attempt in range(retries):
        try:
            return client.generate_content(prompt).text.strip()
        except Exception as exc:
            err = str(exc)
            if "429" in err or "quota" in err.lower():
                m = re.search(r"retry_delay\s*\{\s*seconds:\s*(\d+)", err)
                wait = int(m.group(1)) + 2 if m else 30
                if attempt < retries - 1:
                    time.sleep(wait)
                    continue
            if attempt >= retries - 1:
                raise
    return ""


# ── Action determination ───────────────────────────────────────────────────────
def _determine_action(
    price:      float,
    avg_cost:   float,
    stop_loss:  float,
    take_profit:float,
    geo_score:  float,
    tech_score: float,
    weight:     float,          # 0-1 fraction of portfolio
) -> tuple[str, str, str]:
    """
    Returns (action_label, badge_color, priority).
    Priority: CRITICAL > HIGH > MEDIUM > LOW
    """
    # Hard triggers first (price-based)
    if price <= stop_loss:
        return "STOP-LOSS", "#ff1744", "CRITICAL"

    if price >= take_profit:
        return "TAKE PROFIT", "#00e5ff", "HIGH"

    if weight > CONCENTRATION_THRESHOLD:
        return "PARTIAL SELL", "#ff6d00", "HIGH"

    # Signal-based
    geo_bull  = geo_score  >  0.20
    geo_bear  = geo_score  < -0.20
    tech_bull = tech_score >  0.20
    tech_bear = tech_score < -0.20

    if geo_bull and tech_bull:
        return "INVEST MORE", "#00e676", "MEDIUM"
    if geo_bear and tech_bear:
        return "SELL", "#ff4757", "HIGH"
    if geo_bear or tech_bear:
        return "HOLD", "#ffa500", "LOW"
    return "HOLD", "#ffa500", "LOW"


# ── Per-holding analysis ───────────────────────────────────────────────────────
def analyze_holding(
    holding:         dict,
    price:           float,
    geo_score:       float,
    tech_ind:        dict,
    macro:           dict,
    portfolio_value: float,
) -> dict:
    """
    Analyse a single holding and return prescriptive intelligence.

    holding: {ticker, shares, avg_cost}
    price:   live market price (native currency)
    """
    from ingest import is_indian

    ticker   = holding["ticker"]
    shares   = float(holding["shares"])
    avg_cost = float(holding["avg_cost"])
    mkt_val  = price * shares
    book_val = avg_cost * shares
    pnl      = mkt_val - book_val
    pct_pnl  = pnl / book_val * 100 if book_val else 0.0
    weight   = mkt_val / portfolio_value if portfolio_value else 0.0

    stop_loss   = price  * (1 - STOP_LOSS_PCT)        # trailing: 5% below NOW
    take_profit = avg_cost * (1 + TAKE_PROFIT_PCT)     # fixed:   +15% from cost

    action, badge_color, priority = _determine_action(
        price, avg_cost, stop_loss, take_profit,
        geo_score, tech_ind.get("score", 0.0), weight,
    )

    ccy = "₹" if is_indian(ticker) else "$"
    return {
        "ticker":       ticker,
        "shares":       shares,
        "avg_cost":     avg_cost,
        "price":        price,
        "mkt_val":      round(mkt_val, 2),
        "book_val":     round(book_val, 2),
        "pnl":          round(pnl, 2),
        "pct_pnl":      round(pct_pnl, 2),
        "weight":       round(weight, 4),
        "stop_loss":    round(stop_loss, 2),
        "take_profit":  round(take_profit, 2),
        "action":       action,
        "badge_color":  badge_color,
        "priority":     priority,
        "ccy":          ccy,
        "geo_score":    round(geo_score, 3),
        "tech_score":   round(tech_ind.get("score", 0.0), 3),
        "rsi14":        tech_ind.get("rsi14"),
        "macd":         tech_ind.get("macd"),
        "sma_signal":   tech_ind.get("sma_signal", 0.0),
        "regime":       macro.get("regime", "UNKNOWN"),
        "tech_pressure":macro.get("tech_pressure", 0.0),
    }


# ── Portfolio-level analysis ───────────────────────────────────────────────────
def get_portfolio_analysis(
    holdings:   list[dict],
    prices:     dict,           # {ticker: (price, source)}
    geo_score:  float,
    tech_map:   dict,           # {ticker: tech_ind dict}
    macro:      dict,
) -> dict:
    """
    Analyse all holdings and return portfolio-level intelligence including
    rebalance suggestions.

    Returns:
        {
          total_value, analyses: list[dict],
          rebalance_suggestions: list[str],
          portfolio_action: str,   # overall portfolio signal
        }
    """
    # Compute total portfolio value
    total_value = 0.0
    for h in holdings:
        p, _ = prices.get(h["ticker"], (h["avg_cost"], ""))
        total_value += (p or h["avg_cost"]) * h["shares"]

    analyses = []
    for h in holdings:
        tkr = h["ticker"]
        p, _ = prices.get(tkr, (h["avg_cost"], ""))
        if p is None:
            p = h["avg_cost"]
        ti = tech_map.get(tkr, {"score": 0.0, "rsi14": None, "macd": None,
                                 "sma_signal": 0.0, "uncertainty": 0.2})
        a  = analyze_holding(h, p, geo_score, ti, macro, total_value)
        analyses.append(a)

    # ── Rebalance suggestions ──────────────────────────────────────────────────
    suggestions = []

    # 1. Concentration check
    overweight = [a for a in analyses if a["weight"] > CONCENTRATION_THRESHOLD]
    for a in overweight:
        suggestions.append(
            f"**{a['ticker']}** is {a['weight']:.0%} of portfolio — consider trimming "
            f"to <40% and diversifying proceeds into a low-correlation asset."
        )

    # 2. Geo risk hedge
    if geo_score < -0.35:
        has_gold = any(h["ticker"].upper() in {"GLD", "IAU", "GOLD", "SGLD"} for h in holdings)
        if not has_gold:
            suggestions.append(
                f"Geopolitical Risk is elevated ({geo_score:+.3f}). "
                f"Consider adding **GLD** (SPDR Gold ETF) as a 5-10% hedge position."
            )

    # 3. Single-country concentration
    india_wt = sum(a["weight"] for a in analyses if ".NS" in a["ticker"] or ".BO" in a["ticker"])
    usa_wt   = 1.0 - india_wt
    if india_wt > 0.80:
        suggestions.append(
            "Portfolio is >80% India. Add a US ETF such as **SPY** for geographic diversification."
        )
    if usa_wt > 0.80 and len(holdings) >= 2:
        suggestions.append(
            "Portfolio is >80% USA. Consider a small allocation to Indian equities "
            "(e.g. **RELIANCE.NS** or **TCS.NS**) for emerging-market exposure."
        )

    # 4. All-tech concentration
    tech_tkrs = {"QQQ","NVDA","AAPL","MSFT","META","TSLA","AMZN","TCS.NS","INFY.NS"}
    tech_wt   = sum(a["weight"] for a in analyses if a["ticker"].upper() in tech_tkrs)
    if tech_wt > 0.60 and macro.get("hawkish"):
        suggestions.append(
            f"Tech/growth names are {tech_wt:.0%} of portfolio in a HAWKISH macro regime. "
            f"Consider rotating a portion into **ITA** (defense) or **GLD** (gold)."
        )

    # 5. No hedge when all bearish
    critical = [a for a in analyses if a["priority"] == "CRITICAL"]
    if critical and not suggestions:
        suggestions.append(
            "One or more stop-loss levels triggered — review positions immediately."
        )

    if not suggestions:
        suggestions.append(
            "Portfolio allocation looks balanced. Continue monitoring macro signals."
        )

    # Overall portfolio signal
    action_scores = {
        "INVEST MORE": 2, "HOLD": 1, "PARTIAL SELL": -1,
        "SELL": -2, "STOP-LOSS": -3, "TAKE PROFIT": 1,
    }
    avg_action = (sum(action_scores.get(a["action"], 0) for a in analyses) / len(analyses)
                  if analyses else 0)
    portfolio_action = (
        "Risk-On  — majority of signals bullish"    if avg_action >  0.5 else
        "Risk-Off — majority of signals bearish"    if avg_action < -0.5 else
        "Neutral  — mixed signals across portfolio"
    )

    return {
        "total_value":            round(total_value, 2),
        "analyses":               analyses,
        "rebalance_suggestions":  suggestions,
        "portfolio_action":       portfolio_action,
    }


# ── XAI Reasoning Card ────────────────────────────────────────────────────────
_REASONING_PROMPT = """\
You are an Explainable AI engine for a Stock Oracle system.
Generate a concise reasoning card for the investment decision below.
Output ONLY 4-5 bullet points. Each bullet: "• [Factor]: [1-line explanation]."
Order by impact (most important first). Max 150 words total.

Ticker      : {ticker} ({desc})
Action      : {action}
Veracity    : {v_score:.3f} ({v_label} — NB classifier)
Geo Score   : {geo:+.3f} ({geo_label})
Tech Score  : {tech:+.3f} (RSI-14: {rsi}, MACD: {macd_note}, SMA: {sma_note})
Macro       : {regime} | Fed {fed_rate}% | CPI {cpi_yoy}% | Tech Pressure: {tech_p:.0%}
P&L         : {pnl_pct:+.1f}% (live vs cost basis)
Stop-Loss   : {ccy}{stop_loss:.2f}  |  Take-Profit: {ccy}{take_profit:.2f}
Portfolio Wt: {weight:.0%}{conc_flag}
"""


def _mock_reasoning(a: dict, v_score: float, desc: str) -> str:
    lines = []
    if a["action"] == "STOP-LOSS":
        lines.append(f"• Stop-Loss Triggered: Price {a['ccy']}{a['price']:.2f} breached "
                     f"the -{STOP_LOSS_PCT:.0%} trailing stop at {a['ccy']}{a['stop_loss']:.2f}.")
    if a["action"] == "TAKE PROFIT":
        lines.append(f"• Take-Profit Reached: Price exceeded +{TAKE_PROFIT_PCT:.0%} "
                     f"target ({a['ccy']}{a['take_profit']:.2f}) — consider locking gains.")
    geo_lbl = "Positive" if a["geo_score"] > 0.2 else "Negative" if a["geo_score"] < -0.2 else "Neutral"
    tech_lbl = "Bullish" if a["tech_score"] > 0.2 else "Bearish" if a["tech_score"] < -0.2 else "Neutral"
    lines.append(f"• Geopolitical Risk ({a['geo_score']:+.3f}): {geo_lbl} macro sentiment "
                 f"scored by Gemini 2.5 Flash.")
    lines.append(f"• Technical Signal ({a['tech_score']:+.3f}): {tech_lbl} — "
                 f"RSI-14: {a.get('rsi14','N/A')}.")
    if v_score >= 0.5:
        lines.append(f"• News Veracity ({v_score:.3f}): Headline classified as REAL by NB filter "
                     f"— signal is credible.")
    else:
        lines.append(f"• News Veracity ({v_score:.3f}): Headline flagged as potentially unreliable "
                     f"— treat signal with caution.")
    if a["weight"] > CONCENTRATION_THRESHOLD:
        lines.append(f"• Concentration Risk ({a['weight']:.0%}): Position exceeds 40% threshold — "
                     f"rebalance to reduce single-name exposure.")
    if a["tech_pressure"] > 0.3:
        lines.append(f"• Macro Headwind: FRED signals {a['regime']} regime "
                     f"with {a['tech_pressure']:.0%} tech pressure.")
    return "\n".join(lines[:5])


def generate_reasoning_card(
    ticker:    str,
    a:         dict,            # analyze_holding() output
    v_score:   float,
    macro:     dict,
    desc:      str = "",
) -> str:
    """
    Generate a Gemini-powered XAI reasoning card for one holding.
    Falls back to mock bullets if Gemini unavailable.
    Cached 1h per (ticker, action, geo_rounded).
    """
    geo_r   = round(a["geo_score"], 1)
    tech_r  = round(a["tech_score"], 1)
    cache_k = f"reasoning:{ticker}:{a['action']}:{geo_r}:{tech_r}"
    if cache_k in _cache:
        return _cache[cache_k]

    v_label   = "REAL" if v_score >= 0.5 else "FAKE"
    geo_label = ("Bullish" if a["geo_score"] > 0.2 else
                 "Bearish" if a["geo_score"] < -0.2 else "Neutral")
    rsi14     = a.get("rsi14")
    rsi_str   = f"{rsi14:.1f}" if rsi14 is not None else "N/A"
    macd      = a.get("macd")
    macd_note = (f"{macd:+.3f} (above signal)" if macd and macd > 0
                 else f"{macd:+.3f} (below signal)" if macd else "N/A")
    sma_note  = ("SMA20 > SMA50 (bullish cross)" if a.get("sma_signal", 0) > 0
                 else "SMA20 < SMA50 (bearish cross)")
    conc_flag = "  ⚠ OVERWEIGHT" if a["weight"] > CONCENTRATION_THRESHOLD else ""

    prompt = _REASONING_PROMPT.format(
        ticker=ticker, desc=desc or ticker,
        action=a["action"], v_score=v_score, v_label=v_label,
        geo=a["geo_score"], geo_label=geo_label,
        tech=a["tech_score"], rsi=rsi_str, macd_note=macd_note, sma_note=sma_note,
        regime=macro.get("regime","?"), fed_rate=macro.get("fed_rate","?"),
        cpi_yoy=macro.get("cpi_yoy","?"), tech_p=a.get("tech_pressure",0.0),
        pnl_pct=a["pct_pnl"],
        ccy=a["ccy"], stop_loss=a["stop_loss"], take_profit=a["take_profit"],
        weight=a["weight"], conc_flag=conc_flag,
    )

    try:
        card = _call_gemini(prompt)
        if card:
            _cache.set(cache_k, card, expire=_TTL)
            return card
    except Exception:
        pass

    # Fallback
    card = _mock_reasoning(a, v_score, desc or ticker)
    return card
